    private void showPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showPassActionPerformed
        // TODO add your handling code here:
        show = !show;
        if(show){
            txtPass.setEchoChar((char)0);
            showPass.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Interfaces/resources/hide.png")));
        }
        else{
            txtPass.setEchoChar('●');
            showPass.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Interfaces/resources/show.png")));
        }
    }//GEN-LAST:event_showPassActionPerformed

